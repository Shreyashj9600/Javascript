// While and do while loop 

index = 0;
while(index <= 10){
    console.log(`value of index is ${index}`)
    index = index + 2;
}

let myArr = ['flash','batman','superman']

let arr = 0
while(arr < myArr.length){
    console.log(`Array element is ${myArr[arr]}`)
    arr = arr + 1
}

// do while


let score = 1
do{
    console.log(`value of score is ${score}`)
    score = score + 1
}while(score <= 10)